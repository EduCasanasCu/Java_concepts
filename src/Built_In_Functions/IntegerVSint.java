package Built_In_Functions;

public class IntegerVSint {
    /*
        int => dato primitivo
        Integer => wrapper class, encapsula un valor de tipo int como un objeto.

        int (tipo primitivo)
	        •Definición: Es un tipo de dato primitivo que almacena valores enteros.
	    Características:
	        •No es un objeto, es un dato primitivo.
	        •Es más rápido y ocupa menos memoria en comparación con Integer.
	        •No puede ser null (tiene un valor predeterminado de 0 si no se inicializa).


        Integer (clase envolvente)
	        •Definición: Es una clase que pertenece al paquete java.lang y
	        envuelve un valor int en un objeto.
	    Características:
	        •Es un objeto, lo que significa que tiene métodos y funcionalidades adicionales.
	        •Puede ser null, lo cual es útil en ciertos casos (como trabajar con colecciones
	         que admiten valores nulos, por ejemplo, ArrayList<Integer>).
	        •Es necesario para situaciones en las que se requiere un objeto, como al usar
	        colecciones de Java (por ejemplo, ArrayList o HashMap).
     */
}
